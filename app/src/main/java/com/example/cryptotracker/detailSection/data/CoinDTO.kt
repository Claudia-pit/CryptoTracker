import com.google.gson.annotations.SerializedName

data class CoinInfoDTO(
    @SerializedName("description") val description: Description,
    @SerializedName("id") val id: String,
    @SerializedName("image") val image: Image,
    @SerializedName("market_data") val market_data: MarketDataSingle,
    @SerializedName("name") val name: String,
    @SerializedName("symbol") val symbol: String,
    @SerializedName("links") val links: SingleLink,
) {
    fun toDomain(): SingleCoin {
        return SingleCoin(
            description = description.toDomain(),
            id = id, image = image.toDomain(),
            market_data = market_data.toDomain(),
            name = name,
            symbol = symbol,
            links = links.toDomain()

        )
    }
}


